package class11;

public class Departments {


	public String deptID;
	public String deptName;
			

	public Departments(String deptID, String deptName) {
				
			this.deptID = deptID;
			this.deptName = deptName;
				
	}
	public String getdeptID() {
		return deptID;
	}
			
	public String getdeptName() {
		return deptName;
	}

}




